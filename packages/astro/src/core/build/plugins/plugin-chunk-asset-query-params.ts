import type { Plugin as VitePlugin } from 'vite';
import { ASTRO_VITE_ENVIRONMENT_NAMES } from '../../constants.js';
import type { StaticBuildOptions } from '../types.js';

/**
 * Appends `assetQueryParams` (e.g. `?dpl=<deployment-id>` for Vercel skew protection)
 * to relative JS import specifiers inside client-side chunks.
 *
 * Without this, inter-chunk imports like `import{a}from"./chunk.hash.js"` would resolve
 * without the query parameter, bypassing skew protection for the dependency tree.
 *
 * This plugin uses Rollup's `renderChunk` hook to post-process chunk code after minification,
 * rewriting relative `.js` import specifiers to include the configured query params.
 */
export function pluginChunkAssetQueryParams(options: StaticBuildOptions): VitePlugin | undefined {
	const assetQueryParams = options.settings.adapter?.client?.assetQueryParams;
	if (!assetQueryParams) return undefined;

	const queryString = assetQueryParams.toString();
	if (!queryString) return undefined;

	return {
		name: '@astro/plugin-chunk-asset-query-params',

		applyToEnvironment(environment) {
			return environment.name === ASTRO_VITE_ENVIRONMENT_NAMES.client;
		},

		renderChunk(code) {
			// Match static imports: from"./chunk.hash.js" or from './chunk.hash.js'
			// and dynamic imports: import("./chunk.hash.js") or import('./chunk.hash.js')
			// These are relative JS imports generated by Rollup's code-splitting.
			const staticImportRe = /(from\s*["'])(\.\/[^"']+\.js)(["'])/g;
			const dynamicImportRe = /(import\(\s*["'])(\.\/[^"']+\.js)(["']\s*\))/g;

			let modified = code;
			modified = modified.replace(staticImportRe, `$1$2?${queryString}$3`);
			modified = modified.replace(dynamicImportRe, `$1$2?${queryString}$3`);

			if (modified !== code) {
				return { code: modified, map: null };
			}
		},
	};
}
